    const quantityInput = document.getElementById('quantity');
    const decreaseButtons = document.querySelectorAll('.decrease-quantity');
    const increaseButtons = document.querySelectorAll('.increase-quantity');
    
    if (quantityInput) {
        const minQuantity = parseInt(quantityInput.dataset.min) || 1;
        const multiplicity = parseInt(quantityInput.dataset.multiplicity) || 1;
        
        // Устанавливаем минимальное значение и шаг
        quantityInput.min = minQuantity;
        quantityInput.step = multiplicity;
        
        // Обработчики кнопок
        decreaseButtons.forEach(button => {
            button.addEventListener('click', function() {
                let currentValue = parseInt(quantityInput.value) || minQuantity;
                let newValue = currentValue - multiplicity;
                if (newValue >= minQuantity) {
                    quantityInput.value = newValue;
                    updateTotalPrice();
                }
            });
        });
        
        increaseButtons.forEach(button => {
            button.addEventListener('click', function() {
                let currentValue = parseInt(quantityInput.value) || minQuantity;
                let newValue = currentValue + multiplicity;
                quantityInput.value = newValue;
                updateTotalPrice();
            });
        });
        
        // Обработчик изменения значения вручную
        quantityInput.addEventListener('change', function() {
            let value = parseInt(this.value) || minQuantity;
            // Округляем до ближайшего значения, кратного multiplicity
            let roundedValue = Math.round(value / multiplicity) * multiplicity;
            // Убедимся, что значение не меньше минимального
            roundedValue = Math.max(minQuantity, roundedValue);
            this.value = roundedValue;
            updateTotalPrice();
        });
        
        // Обработчик ввода для валидации
        quantityInput.addEventListener('input', function() {
            // Можно добавить валидацию в реальном времени
